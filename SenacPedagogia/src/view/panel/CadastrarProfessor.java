    private void cadastrar(){
            try {
                ProfessorVO pVO = new ProfessorVO();

                pVO.setNome(jtfNomeValue.getText());
                pVO.setEmail(jtfEmailValue.getText());
                pVO.setData_nascimento(jtfDataNascValue.getText());
                pVO.setEndereco(jtfEnderecoValue.getText());
                pVO.setTelefone(jtfTelefoneValue.getText());
                pVO.setCpf(jtfCpfValue.getText());

            
                /*
                Enviar o objeto pVO para o banco de dados,
                mas o envio √© atrav√©s da Servicos,
                por√©m antes passa pela DAO.
                */
                ProfessorServicos ps = servicos.ServicosFactory.getProfessorServicos();

                //Chamndo o m√©todo
                ps.cadastrarProfessor(pVO);

                //Retorno para o usu√°rio
                JOptionPane.showMessageDialog(this, "Professor Cadastrado com sucesso!");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro! GUICadProfessors " + e.getMessage());
            }
        }//fim do m√©todo cadastrar
    
